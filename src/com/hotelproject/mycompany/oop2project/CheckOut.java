    public CheckOut(Employee employee) {
        this.employee = employee;
        initComponents();
        this.setLocationRelativeTo(null);

        ArrayList<Guest> guests = Guest.getAllGuests();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        for (int i = 0; i < guests.size(); i++) {
            Guest guest = guests.get(i);

            if (HotelServices.getGuestBookingID(guest.getUserID()) != -1) {
                BookingRoom bookingRoom = BookingRoom.getGuestBookingRoom(guest.getUserID());
                Object[] row = new Object[]{HotelServices.getGuestBookingID(guest.getUserID()), guest.getName(), bookingRoom.getCheckInDate(), bookingRoom.getCheckOutDate(), (guest.getCheckOut() ? "Checked Out" : (guest.getCheckIn() ? "Checked In" : "Not Checked In Still"))};
                model.addRow(row);
            }

        }
    }

