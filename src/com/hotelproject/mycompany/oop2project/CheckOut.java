    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        if (row != -1) {
            int bookingID = (Integer) jTable1.getValueAt(row, 0);
            String userID = RequestGandH.getGuestUserID(bookingID);
            if (userID != null) {
                if (Guest.getGuest(userID).getCheckIn()) {
                    if (!Guest.getGuest(userID).getCheckOut()) {
                        Guest.guestCheckOut(true, userID);
                        jTable1.setValueAt("Checked Out", row, 4);
                        JOptionPane.showMessageDialog(null, "Guest with bookingID " + bookingID + " is Check out!!!");
                    } else {
                        JOptionPane.showMessageDialog(null, "Guest with bookingID " + bookingID + " has already Check out!!!");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Guest with bookingID " + bookingID + " does not Check In Still!!!");
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please select the guest!!!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

